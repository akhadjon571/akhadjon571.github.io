function setLanguage(lang) {
  localStorage.setItem("language", lang)
  const t = translations[lang]

  // Update HTML lang attribute
  document.documentElement.lang = lang

  // Update the select element's value
  const languageSelect = document.querySelector(".language-select")
  if (languageSelect) {
    languageSelect.value = lang
  }

  // Hero section
  document.getElementById("greeting").innerHTML = t.greeting
  document.getElementById("description").textContent = t.description

  // Navigation
  document.getElementById("nav-home").textContent = t.nav.home
  document.getElementById("nav-about").textContent = t.nav.about
  document.getElementById("nav-skills").textContent = t.nav.skills
  document.getElementById("nav-projects").textContent = t.nav.projects
  document.getElementById("nav-contact").textContent = t.nav.contact

  // Hero buttons
  document.getElementById("btn-projects").textContent = t.btnProjects
  document.getElementById("btn-contact").textContent = t.btnContact

  // About section
  document.getElementById("about-title").textContent = t.aboutTitle
  document.getElementById("about-subtitle").textContent = t.aboutSubtitle
  document.getElementById("about-heading").textContent = t.aboutHeading
  document.getElementById("about-p1").textContent = t.aboutP1
  document.getElementById("about-p2").textContent = t.aboutP2
  document.getElementById("stat-projects-text").textContent = t.statProjectsText
  document.getElementById("stat-experience-text").textContent = t.statExperienceText

  // Skills section
  document.getElementById("skills-title").textContent = t.skillsTitle
  document.getElementById("skills-subtitle").textContent = t.skillsSubtitle
  document.getElementById("skill-frontend").textContent = t.skillCategories.frontend
  document.getElementById("skill-backend").textContent = t.skillCategories.backend
  document.getElementById("skill-tools").textContent = t.skillCategories.tools

  // Projects section
  document.getElementById("projects-title").textContent = t.projectsTitle
  document.getElementById("projects-subtitle").textContent = t.projectsSubtitle

  // Project cards - CORRECTED to use title instead of status
  document.getElementById("project-ecommerce-title").textContent = t.projectItems.ecommerce.title
  document.getElementById("project-ecommerce-desc").textContent = t.projectItems.ecommerce.description
  document.getElementById("project-portfolio-title").textContent = t.projectItems.portfolio.title
  document.getElementById("project-portfolio-desc").textContent = t.projectItems.portfolio.description
  document.getElementById("project-mobile-title").textContent = t.projectItems.mobile.title
  document.getElementById("project-mobile-desc").textContent = t.projectItems.mobile.description

  // Project links
  document.querySelectorAll(".project-view-link").forEach((link) => {
    link.textContent = t.projectLinks.view
  })
  document.querySelectorAll(".project-github-link").forEach((link) => {
    link.textContent = t.projectLinks.github
  })

  // Contact section
  document.getElementById("contact-title").textContent = t.contactTitle
  document.getElementById("contact-subtitle").textContent = t.contactSubtitle
  document.getElementById("contact-info-title").textContent = t.contactInfoTitle

  // Contact details
  document.getElementById("contact-email-label").textContent = t.contactDetails.email
  document.getElementById("contact-phone-label").textContent = t.contactDetails.phone
  document.getElementById("contact-telegram-label").textContent = t.contactDetails.telegram
  document.getElementById("contact-address-label").textContent = t.contactDetails.address
  document.getElementById("contact-location").textContent = t.contactDetails.location

  // Form labels
  document.getElementById("label-name").textContent = t.labelName
  document.getElementById("label-email").textContent = t.labelEmail
  document.getElementById("label-message").textContent = t.labelMessage
  document.getElementById("btn-submit").textContent = t.btnSubmit

  // Footer
  document.getElementById("footer-title").textContent = t.footerTitle
  document.getElementById("footer-subtitle").textContent = t.footerSubtitle
  document.getElementById("footer-copy").innerHTML = t.footerCopy

  // Store form success message for later use
  window.currentFormSuccessMessage = t.formSuccess
}

window.onload = () => {
  const savedLang = localStorage.getItem("language") || "uz"
  setLanguage(savedLang)
}
